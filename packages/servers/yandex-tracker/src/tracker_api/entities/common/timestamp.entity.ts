/**
 * Стандартные поля дат (timestamps)
 *
 * Используются во всех entities, которые имеют дату создания и обновления:
 * - Comment
 * - Link
 * - Attachment
 * - Worklog
 * - Project
 * - Component
 * - Queue (расширенная версия)
 *
 * ВАЖНО: Яндекс.Трекер API v3 возвращает даты в формате ISO 8601
 * @example "2024-01-15T10:30:45.123+03:00"
 */

/**
 * Стандартные поля времени создания и обновления
 *
 * ВАЖНО: Все поля обязательны - API всегда возвращает даты для объектов.
 */
export interface TimestampFields {
  /**
   * Дата и время создания объекта (ISO 8601)
   *
   * @example "2024-01-15T10:30:45.123+03:00"
   */
  readonly createdAt: string;

  /**
   * Дата и время последнего обновления объекта (ISO 8601)
   *
   * @example "2024-01-16T15:20:30.456+03:00"
   */
  readonly updatedAt: string;
}
